SELECT
	 IDLAN, NUMERODOCUMENTO, codcfo, HISTORICO,  DATABAIXA, VALORBAIXADO
FROM FLAN WHERE CODCOLIGADA = 1 AND NFOUDUP <> 1 AND (NUMBLOQUEIOS = 0 OR NUMBLOQUEIOS IS NULL) 
AND (BAIXAAUTORIZADA IS NULL OR BAIXAAUTORIZADA <> 0) AND PAGREC = 2 AND CODCOLCFO = 1 AND CODCFO in('F00389','F00117')
AND STATUSLAN = 1 
AND NOT EXISTS (SELECT * FROM FRELLAN R (NOLOCK) WHERE (FLAN.IDLAN = R.IDLAN OR FLAN.IDLAN = R.IDLANREL) AND R.TIPOREL = 2 AND R.CODCOLIGADA = 1) 
AND CODTDO IN (SELECT CODTDO FROM FTDO WHERE CODCOLIGADA = 1 AND EDEVOLUCAO = 2) AND VALORADIANTAMENTO = 0 AND VALORDEVOLUCAO = 0
order by codcfo, DATABAIXA




