SELECT '1', DATA, CREDITO AS CODCONTA, VALOR 
FROM CPARTIDA
WHERE 
	DATA >= '08-01-2013' AND DATA <= (SELECT convert(date, texto, 103) FROM CPARAM
WHERE ID = 90) AND CREDITO IS NOT NULL AND CREDITO >= '3' AND CREDITO < '5' 
AND CPARTIDA.CODHISTP not in (Select CODHISTP from CHISTP where HISTFECHA = 1) 
AND CPARTIDA.CREDITO NOT IN ('3.1.02.003.0001.0006','3.1.02.003.0001.0007','3.1.02.003.0001.0008')
AND (CPARTIDA.CODCCUSTO NOT IN (SELECT CODCCUSTO FROM GCCUSTO WHERE CODREDUZIDO LIKE '51%') or CPARTIDA.CODCCUSTO is null)

union all 

SELECT '2', DATA, DEBITO AS CODCONTA, VALOR 
FROM CPARTIDA
WHERE 
	DATA >= '08-01-2013' AND DATA <= (SELECT convert(date, texto, 103) FROM CPARAM
WHERE ID = 90) AND DEBITO IS NOT NULL  AND DEBITO >= '3' AND DEBITO < '5'
AND	CPARTIDA.CODHISTP not in (Select CODHISTP from CHISTP where HISTFECHA = 1) 
AND CPARTIDA.DEBITO NOT IN ('3.1.02.003.0001.0006','3.1.02.003.0001.0007','3.1.02.003.0001.0008')
AND (CPARTIDA.CODCCUSTO NOT IN (SELECT CODCCUSTO FROM GCCUSTO WHERE CODREDUZIDO LIKE '51%') or CPARTIDA.CODCCUSTO is null)


