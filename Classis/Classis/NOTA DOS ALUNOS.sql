SELECT
	MATALUNO, EACOMPL.A_NOMEREDUZIDO 
	A1, AD1, F1, A2, AD2, F2, A3, AD3, F3,
	A4, AD4, F4, A5, AD5, F5, A6, AD6, F6,
	A7, AD7, F7, A8, AD8, F8, A9, AD9, F9,
	A10, AD10, F10, A0, AD0, F0,
	PERCFREQ, USITMAT.DESCRICAO
FROM
	UMATALUN
	INNER JOIN USITMAT ON (USITMAT.CODSITMAT = UMATALUN.STATUS)
	INNER JOIN EACOMPL ON (UMATALUN.MATALUNO = EACOMPL.MATRICULA)
WHERE
	CODTUR = '6B'
	AND CODMAT = '206BHG'
	AND MATALUNO <> '00177'
